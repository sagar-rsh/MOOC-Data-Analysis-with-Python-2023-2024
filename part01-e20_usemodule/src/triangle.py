from math import hypot

def hypotenuse(a, b):
  '''
  Returns hypotenuse of a right-angled triangle
  '''
  return hypot(a, b)


def area(a, b):
  '''
  Returns area of the right-angled triangle
  '''
  return 0.5 * a * b

